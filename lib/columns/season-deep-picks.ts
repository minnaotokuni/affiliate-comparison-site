export function seasonDeepPicksForMonth(month: number): SeasonDeepPickDef[] {
  const m = Math.min(12, Math.max(1, month));
  const triplet = SEASON_MONTH_SLUGS[m];
  const out: SeasonDeepPickDef[] = [];
  for (const slug of triplet) {
    const def = SEASON_DEEP_BY_SLUG[slug];
    if (!def) continue;
    const recipes = seasonRecipesForSlug(slug);
    out.push(recipes.length > 0 ? { ...def, recipes } : def);
  }
  return out;
}

/** 野菜別ガイドに同名セクションがあるときだけ hash を返す（アスパラ等は未掲載のため null） */
export function vegetablesGuideAnchorSlug(pick: SeasonDeepPickDef): string | null {
  if (pick.kind !== "vegetable") return null;
  if (pick.slug === "fruit-tomato" || pick.slug === "mini-tomato") return "tomato";
  return profileBySlug(pick.slug) ? pick.slug : null;
}
